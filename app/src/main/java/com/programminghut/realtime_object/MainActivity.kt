package com.programminghut.realtime_object

import android.annotation.SuppressLint
import android.content.Context
import android.content.pm.PackageManager
import android.graphics.*
import android.hardware.camera2.CameraCaptureSession
import android.hardware.camera2.CameraDevice
import android.hardware.camera2.CameraManager
import android.os.Bundle
import android.os.Handler
import android.os.HandlerThread
import android.speech.tts.TextToSpeech
import android.view.Surface
import android.view.TextureView
import android.widget.ImageView
import androidx.appcompat.app.AppCompatActivity
import androidx.core.content.ContextCompat
import com.programminghut.realtime_object.ml.SsdMobilenetV11Metadata1
import org.tensorflow.lite.support.common.FileUtil
import org.tensorflow.lite.support.image.ImageProcessor
import org.tensorflow.lite.support.image.TensorImage
import org.tensorflow.lite.support.image.ops.ResizeOp
import java.util.*
import java.util.concurrent.LinkedBlockingQueue

class MainActivity : AppCompatActivity(), TextToSpeech.OnInitListener, TextToSpeech.OnUtteranceCompletedListener {

    lateinit var labels: List<String>
    var colors = listOf(
        Color.BLUE, Color.GREEN, Color.RED, Color.CYAN, Color.GRAY, Color.BLACK,
        Color.DKGRAY, Color.MAGENTA, Color.YELLOW, Color.RED
    )
    val paint = Paint()
    lateinit var imageProcessor: ImageProcessor
    lateinit var bitmap: Bitmap
    lateinit var imageView: ImageView
    lateinit var cameraDevice: CameraDevice
    lateinit var handler: Handler
    lateinit var cameraManager: CameraManager
    lateinit var textureView: TextureView
    lateinit var model: SsdMobilenetV11Metadata1
    lateinit var textToSpeech: TextToSpeech
    private val utteranceQueue: Queue<String> = LinkedBlockingQueue()
    private var detectedObjectsSet: MutableSet<String> = mutableSetOf()

    // Predefined volumes of shelves (example values, these should be actual volumes in cubic units)
    private val shelfVolumes = listOf(10000.0, 20000.0, 30000.0, 40000.0)

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)
        get_permission()

        labels = FileUtil.loadLabels(this, "labels.txt")
        imageProcessor = ImageProcessor.Builder()
            .add(ResizeOp(300, 300, ResizeOp.ResizeMethod.BILINEAR)).build()
        model = SsdMobilenetV11Metadata1.newInstance(this)
        val handlerThread = HandlerThread("videoThread")
        handlerThread.start()
        handler = Handler(handlerThread.looper)

        imageView = findViewById(R.id.imageView)
        textureView = findViewById(R.id.textureView)
        textureView.surfaceTextureListener = object : TextureView.SurfaceTextureListener {
            override fun onSurfaceTextureAvailable(p0: SurfaceTexture, p1: Int, p2: Int) {
                open_camera()
            }

            override fun onSurfaceTextureSizeChanged(p0: SurfaceTexture, p1: Int, p2: Int) {}
            override fun onSurfaceTextureDestroyed(p0: SurfaceTexture): Boolean {
                return false
            }

            override fun onSurfaceTextureUpdated(p0: SurfaceTexture) {
                bitmap = textureView.bitmap!!
                var image = TensorImage.fromBitmap(bitmap)
                image = imageProcessor.process(image)

                val outputs = model.process(image)
                val locations = outputs.locationsAsTensorBuffer.floatArray
                val classes = outputs.classesAsTensorBuffer.floatArray
                val scores = outputs.scoresAsTensorBuffer.floatArray

                val mutable = bitmap.copy(Bitmap.Config.ARGB_8888, true)
                val canvas = Canvas(mutable)

                val h = mutable.height
                val w = mutable.width
                paint.textSize = h / 15f
                paint.strokeWidth = h / 85f

                var highestScore = 0.0f
                var bestLabel = ""
                var bestBoundingBox: RectF? = null

                scores.forEachIndexed { index, fl ->
                    val x = index * 4
                    if (fl > 0.5) {
                        paint.color = colors[index % colors.size]
                        paint.style = Paint.Style.STROKE
                        val rect = RectF(
                            locations[x + 1] * w, locations[x] * h, locations[x + 3] * w, locations[x + 2] * h
                        )
                        canvas.drawRect(rect, paint)
                        paint.style = Paint.Style.FILL
                        val label = labels[classes[index].toInt()]
                        canvas.drawText("$label $fl", rect.left, rect.top, paint)
                        if (fl > highestScore) {
                            highestScore = fl
                            bestLabel = label
                            bestBoundingBox = rect
                        }
                    }
                }

                imageView.setImageBitmap(mutable)

                if (bestLabel.isNotEmpty() && bestBoundingBox != null && !detectedObjectsSet.contains(bestLabel)) {
                    detectedObjectsSet.add(bestLabel)

                    bestBoundingBox?.let { boundingBox ->
                        // Calculate object volume
                        val objectVolume = calculateVolume(boundingBox, 1.0) // 10.0 is an example depth

                        // Compare with shelf volumes
                        val volumeComparisonResult = compareVolumeWithShelves(objectVolume)
                        val utterance = if (volumeComparisonResult) {
                            "The object detected is a $bestLabel and it fits in one of our shelves."
                        } else {
                            "The object detected is a $bestLabel and there is no place for it in our shelves."
                        }
                        utteranceQueue.offer(utterance)
                        if (!textToSpeech.isSpeaking) {
                            speakNext()
                        }
                    }
                }
            }
        }

        cameraManager = getSystemService(Context.CAMERA_SERVICE) as CameraManager

        textToSpeech = TextToSpeech(this, this)
    }

    override fun onDestroy() {
        super.onDestroy()
        model.close()
        textToSpeech.stop()
        textToSpeech.shutdown()
    }

    @SuppressLint("MissingPermission")
    fun open_camera() {
        cameraManager.openCamera(cameraManager.cameraIdList[0], object : CameraDevice.StateCallback() {
            override fun onOpened(p0: CameraDevice) {
                cameraDevice = p0

                val surfaceTexture = textureView.surfaceTexture
                val surface = Surface(surfaceTexture)

                val captureRequest = cameraDevice.createCaptureRequest(CameraDevice.TEMPLATE_PREVIEW)
                captureRequest.addTarget(surface)

                cameraDevice.createCaptureSession(listOf(surface), object : CameraCaptureSession.StateCallback() {
                    override fun onConfigured(p0: CameraCaptureSession) {
                        p0.setRepeatingRequest(captureRequest.build(), null, null)
                    }

                    override fun onConfigureFailed(p0: CameraCaptureSession) {}
                }, handler)
            }

            override fun onDisconnected(p0: CameraDevice) {}
            override fun onError(p0: CameraDevice, p1: Int) {}
        }, handler)
    }

    fun get_permission() {
        if (ContextCompat.checkSelfPermission(this, android.Manifest.permission.CAMERA) != PackageManager.PERMISSION_GRANTED) {
            requestPermissions(arrayOf(android.Manifest.permission.CAMERA), 101)
        }
    }

    override fun onRequestPermissionsResult(requestCode: Int, permissions: Array<out String>, grantResults: IntArray) {
        super.onRequestPermissionsResult(requestCode, permissions, grantResults)
        if (grantResults[0] != PackageManager.PERMISSION_GRANTED) {
            get_permission()
        }
    }

    override fun onInit(status: Int) {
        if (status == TextToSpeech.SUCCESS) {
            textToSpeech.language = Locale.US
            textToSpeech.setOnUtteranceCompletedListener(this)
        }
    }

    override fun onUtteranceCompleted(utteranceId: String?) {
        speakNext()
    }

    private fun speakNext() {
        if (utteranceQueue.isNotEmpty()) {
            val nextUtterance = utteranceQueue.poll()
            textToSpeech.speak(nextUtterance, TextToSpeech.QUEUE_FLUSH, null, nextUtterance)
        }
    }

    private fun calculateVolume(boundingBox: RectF, depth: Double): Double {
        val width = boundingBox.width().toDouble()
        val height = boundingBox.height().toDouble()
        return width * height * depth
    }

    private fun compareVolumeWithShelves(objectVolume: Double): Boolean {
        return shelfVolumes.any { objectVolume <= it }
    }
}
